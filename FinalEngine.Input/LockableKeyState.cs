namespace FinalEngine.Input
{
    /// <summary>
    ///   Enumerates the available states a lockable key can be in.
    /// </summary>
    public enum LockableKeyState
    {
        /// <summary>
        ///   Specifies the state of the key is locked.
        /// </summary>
        Locked,

        /// <summary>
        ///   Specifies the state of the key is unlocked.
        /// </summary>
        Unlocked
    }
}