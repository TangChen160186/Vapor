namespace FinalEngine.Input
{
    /// <summary>
    ///   Enumerates the available states of a lockable key.
    /// </summary>
    public enum LockableKeyState
    {
        /// <summary>
        ///   Specifies the key is in a locked state.
        /// </summary>
        Locked,

        /// <summary>
        ///   Specifies the key is in an unlocked state.
        /// </summary>
        Unlocked
    }
}